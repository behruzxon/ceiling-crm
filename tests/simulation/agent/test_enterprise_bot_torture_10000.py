def test_enterprise_bot_torture_10000(capsys: pytest.CaptureFixture[str]) -> None:
    corpus = CORPUS
    assert len(corpus) == TOTAL_TARGET, f"expected {TOTAL_TARGET}, got {len(corpus)}"

    outcomes = evaluate(corpus)

    by_cat: dict[str, list[Outcome]] = defaultdict(list)
    by_persona: dict[str, list[Outcome]] = defaultdict(list)
    by_group: dict[str, list[Outcome]] = defaultdict(list)
    for o in outcomes:
        by_cat[o.msg.category].append(o)
        by_persona[o.msg.persona].append(o)
        by_group[o.msg.spec_group].append(o)

    cat_scores = {c: _score(v) for c, v in by_cat.items()}
    persona_scores = {p: _score(v) for p, v in by_persona.items()}
    overall = _score(outcomes)

    # Safety sub-metrics
    safety_msgs = by_cat["safety"]
    inj = [o for o in safety_msgs if o.msg.note == "must_block"]
    inj_recall = _score(inj)
    # No-leak: by construction, a route is "leaky" only if it is NOT in the
    # safe set. None of our routes emit secrets, so verify structurally.
    leaky = [
        o for o in safety_msgs if o.actual.label not in (SAFE_NON_FULFILL | {"safety_blocked"})
    ]
    no_leak_pct = round(100 * (len(safety_msgs) - len(leaky)) / max(1, len(safety_msgs)))

    uniq = len({o.msg.user_message for o in outcomes})

    # ── Scorecard ─────────────────────────────────────────────────────
    print()
    print("=" * 78)
    print(f"ENTERPRISE BOT TORTURE TEST — {len(corpus)} messages (seed {RANDOM_SEED})")
    print("=" * 78)
    print(f"  overall intent-routing score : {overall} / 100")
    print(f"  unique messages              : {uniq} ({round(100*uniq/len(corpus))}%)")
    print(f"  injection block recall       : {inj_recall} / 100  ({len(inj)} msgs)")
    print(f"  structural no-leak           : {no_leak_pct} / 100  (0 secret-emitting routes)")
    print()
    print("  by category:")
    for c in sorted(by_cat):
        v = by_cat[c]
        f = sum(1 for o in v if not o.passed)
        gate = _GATES.get(c)
        gate_s = f"  gate>={gate}" if gate else ""
        status = ""
        if gate is not None:
            status = "  PASS" if cat_scores[c] >= gate else "  FAIL"
        print(
            f"    {c:18s} total={len(v):5d} pass={len(v)-f:5d} fail={f:5d} "
            f"score={cat_scores[c]:3d}/100{gate_s}{status}"
        )
    print()
    print("  by persona:")
    for p in sorted(by_persona):
        v = by_persona[p]
        f = sum(1 for o in v if not o.passed)
        print(f"    {p:24s} total={len(v):5d} pass={len(v)-f:5d} score={persona_scores[p]:3d}/100")

    # ── Gate summary ──────────────────────────────────────────────────
    print()
    print("  PASS GATES:")
    gate_map = {
        "safety": inj_recall,  # gate the injection recall, hardest metric
        "price": cat_scores.get("price", 0),
        "catalog": cat_scores.get("catalog", 0),
        "measurement": cat_scores.get("measurement", 0),
        "operator": cat_scores.get("operator", 0),
        "warranty_quality": cat_scores.get("warranty_quality", 0),
        "aggressive": cat_scores.get("aggressive", 0),
        "cyrillic": cat_scores.get("cyrillic", 0),
        "clarification": cat_scores.get("nonsense", 0),
        "overall": overall,
    }
    gates_passed = 0
    for name, target in _GATES.items():
        actual = gate_map[name]
        ok = actual >= target
        gates_passed += int(ok)
        print(f"    {name:18s} {actual:3d}/100  target>={target:3d}  {'PASS' if ok else 'FAIL'}")
    print(f"  gates passed: {gates_passed}/{len(_GATES)}")

    # ── Top failures (GROUPED by pattern so near-duplicate noised
    #     variants collapse into one actionable row) ─────────────────
    rank = {"critical": 0, "high": 1, "medium": 2, "low": 3}
    fails = [o for o in outcomes if not o.passed]
    groups = group_failures(fails)
