# ── Deterministic generation helpers ───────────────────────────────────────


def _rng() -> random.Random:
    return random.Random(RANDOM_SEED)


def _fill(rng: random.Random, builders: list, target: int) -> list[Msg]:
    """Deterministically expand a pool of (text, meta) builders to `target`
    messages. Builders are tuples produced by category functions; this just
    cycles + lightly varies them with a seeded RNG to reach the count while
    keeping most messages unique."""
    pool: list[tuple] = list(builders)
    rng.shuffle(pool)
    out: list[tuple] = []
    i = 0
    # Cosmetic noise that a real customer would add — keeps duplicates rare
    # without changing the intent.
    # NB: prefixes/suffixes are intent-neutral fillers a real customer adds.
    # "salom " is deliberately excluded — it collides with the greeting
    # detector and would contaminate every category. (Greeting greediness is
    # probed explicitly, not via random noise.)
    prefixes = ("", "", "", "aka ", "iltimos ", "hop ", "ustoz ", "uka ")
    suffixes = ("", "", "", " ?", "...", " axir", " iltimos", " aytinglar")
    while len(out) < target:
        text, meta = pool[i % len(pool)]
        if i >= len(pool):
            # Add deterministic cosmetic noise on repeats only.
            pre = prefixes[rng.randrange(len(prefixes))]
            suf = suffixes[rng.randrange(len(suffixes))]
            text = f"{pre}{text}{suf}"
        out.append((text, meta))
        i += 1
    return [_mk(idx, text, meta) for idx, (text, meta) in enumerate(out)]


def _mk(idx: int, text: str, meta: dict) -> Msg:
    return Msg(
        id=idx,
        persona=meta["persona"],
        category=meta["category"],
        spec_group=meta["spec_group"],
        user_message=text,
        expected_intent=meta["expected_intent"],
        accept=meta["accept"],
        severity=meta["severity"],
        expected_language_style=meta["lang"],
        lead_loss_risk=meta["lead_loss_risk"],
        safety_risk=meta["safety_risk"],
        note=meta.get("note", ""),
        should_ask_clarification=meta.get("clarify", False),
        should_route_to_safety=meta.get("safety", False),
    )


def _meta(**kw) -> dict:
    base = {
        "persona": "normal_customer",
        "lang": "uz_latin",
        "lead_loss_risk": "low",
        "safety_risk": "none",
        "severity": "medium",
        "clarify": False,
        "safety": False,
    }
    base.update(kw)
    return base


